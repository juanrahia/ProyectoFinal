    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String claveAntigua = new String(jPasswordField1.getPassword());
        String claveNueva = new String(jPasswordField2.getPassword());
        String claveRepetida = new String(jPasswordField3.getPassword());
        String claveProperties = properties.getProperty("key");
        
        //Si ha incluido la clave correcta
        if(claveProperties.equals(claveAntigua)){
            //Si cumple las condiciones(!) se inserta los valores en el property
            if(claveNueva.equals(claveRepetida)){
                properties.setProperty("key", claveNueva);
                properties.setProperty("esNuevo","false");
                try {
                    properties.store(new BufferedWriter(new FileWriter(PROPERTIES_FILE)), "");
                } catch (IOException ex) {
                    Logger.getLogger(RegistroUsuario.class.getName()).log(Level.SEVERE, null, ex);
                }
                this.dispose();
            }
        }else{
            JOptionPane.showMessageDialog(null, "Clave incorrecta");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

