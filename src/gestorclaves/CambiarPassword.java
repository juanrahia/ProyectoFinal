    public void limiteCaracteres(){
        //Objeto Document para limitar los caracteres de los campos
        DocumentCharactersLimiter limitClave = new DocumentCharactersLimiter();
        DocumentCharactersLimiter limitClaveRepetida = new DocumentCharactersLimiter();
        DocumentCharactersLimiter limitClaveAntigua = new DocumentCharactersLimiter();
        //Se establecen los limites de caracteres
        limitClave.setLimit(20);
        limitClaveRepetida.setLimit(20);
        limitClaveAntigua.setLimit(20);
        //Se aplican a los campos especificos
        jPasswordField1.setDocument(limitClaveAntigua);
        jPasswordField2.setDocument(limitClave);
        jPasswordField3.setDocument(limitClaveRepetida);
    }
